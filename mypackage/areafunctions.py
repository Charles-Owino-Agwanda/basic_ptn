def rectangle(w, h):
    """
    Returns the area of a rectangle

    Args:
        w (int): width
        h (int): height

    Returns:
        int: the area of a rectangle
    """
    return w * h


def circle(r):
    """
    returns the area of a circle

    Args:
        r (int): radius of a circle
    Returns:
        float: The radius of the circle
    """
    return 3.142 * r * r
