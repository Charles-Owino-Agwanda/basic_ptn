def sum(x, y):
    """
    returns the sum of two values

    Args:
        x (int): The value of x
        y (int): The value of y

    Returns:
        int: The sum of two values
    """
    return x + y


def average(x, y):
    """
    returns the average of two values

    Args:
        x (int): The value of x
        y (_type_): The value of y

    Returns:
        float: The average of two values
    """
    return (x + y) / 2


def power(x, y):
    """
    returns x raised to power y

    Args:
        x (int): The value of x
        y (int): The value of y

    Returns:
        int: The result of power
    """
    return x**y
